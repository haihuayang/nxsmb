static void test_gssapi()
{
	GSSAPIContextToken ct;
	memset(&ct, 0, sizeof ct);
	size_t consumed;
	int ret = x_asn1_decode(ct, ntlmssp_negotiation, sizeof ntlmssp_negotiation, &consumed);
	assert(ret == 0);
	assert(consumed == sizeof ntlmssp_negotiation);
}

static void test_gss_spnego()
{
	gss_buffer_desc input{sizeof(ntlmssp_negotiation), (void *)ntlmssp_negotiation};
	gss_buffer_desc data;
	int ret = gss_decapsulate_token(&input, GSS_SPNEGO_MECHANISM, &data);
	assert(ret == 0);

	NegotiationToken nt;
	size_t consumed;
	ret = x_asn1_decode(nt, (const uint8_t *)data.value, data.length, &consumed);
	assert(ret == 0);
	assert(nt.element == NegotiationToken::choice_NegotiationToken_negTokenInit);

	const auto &negInit = nt.u.negTokenInit;
	struct ntlm_type1 ntlm;
	
	struct ntlm_buf buf{negInit.mechToken->length, negInit.mechToken->data};
	ret = heim_ntlm_decode_type1(&buf, &ntlm);
	assert(ret == 0);
}

